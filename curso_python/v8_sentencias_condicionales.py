# Condicional simple
edad = 15
if edad < 18:
	print("Menor de edad")

# Condicional if-else
temperatura = "frío"
if temperatura != "calor":
	print("Hace frío")
else:
	print("Hace calor")

# Condicional elif
estacion = "Otoño"
if estacion == "Primavera":
	print("Primavera")
elif estacion == "Verano":
	print("Verano")
elif estacion == "Otoño":
	print("Otoño")
else:
	print("Invierno")
	